package Beginner_1.maths;
/*Write a program that takes a variable named input and
assign its value as 8 and prints its multiplication table upto 10.*/
public class tables {
    public static void main(String[] args) {
        int s=8;
        for(int i=1;i<=10;i++){
            System.out.println(s+" x "+i+" = "+s*i);
        }
    }
}
